a =10
b = 5

# Arithmetic Operators
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)

# Modulo, Exponentiation, Floor Division
print("a % b =", a % b)
print("a ** b =", a ** b)
print("a // b =", a // b)

# Comparison Operators
print("a > b =", a > b)
print("a < b =", a < b)
print("a == b =", a == b)
print("a != b =", a != b)
print("a >= b =", a >= b)
print("a <= b =", a <= b)

print("a > 5 and b < 10 =", a > 5 and b < 10)
print("a > 5 or b < 3 =", a > 5 or b < 3)
print("not (a > 5) =", not (a > 5))
